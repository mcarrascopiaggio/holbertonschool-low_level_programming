#include "main.h"

/**
 * add - Adds two integers and returns the result.
 *
 * @a: number one.
 * @b: number two.
 *
 * Return: Add of number one and number two.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Substracts two integers and returns the result.
 *
 * @a: number one.
 * @b: number two.
 *
 * Return: Add of number one and number two.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - Divides two integers and returns the result.
 *
 * @a: number one.
 * @b: number two.
 *
 * Return: Add of number one and number two.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - Multiplies two integers and returns the result.
 *
 * @a: number one.
 * @b: number two.
 *
 * Return: Multiple of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - returns mod of two integers
 *
 * @a: number one.
 * @b: number two.
 *
 * Return: Add of number one and number two.
 */
int mod(int a, int b)
{
	return (a % b);
}
